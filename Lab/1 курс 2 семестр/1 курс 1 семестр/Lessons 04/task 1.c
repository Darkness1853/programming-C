#include <stdio.h>

int main() {
    int n;
    printf("Введите размер матрицы n: ");
    scanf("%d", &n);
    
    int i = 1; //счетчик
    for (int a = 0; a < n; a++) { // Цикл для строк
        printf(".\n."); //разделитель для каждой строки
        for (int b = 0; b < n; b++) { // Цикл для столбцов
            printf(".%d.", i++); //Печатаем текущее значение +1
        }
    }
    printf("\n.."); 
    return 0;
}
