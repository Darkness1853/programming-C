#include <stdio.h>
// Функция для проверки, является ли число простым. делиться ли это число только на себя и на 1.
int isPrime(int num) {
    if (num <= 1) return 0; // Числа <= 1 не являются простыми.Если num меньше или равно 1,функция возвращает ноль
    for (int i = 2; i * i <= num; i++) {//цикл который проверяет на делимость
        if (num % i == 0) return 0; // Если делится на i, то не простое
    }
    return 1; // Если не найдено делителей, то простое
}

int main() {
    int arrX[] = {34,2,3,4,55,12,7}; //Массив,данные можно изменить
    int sizeX = sizeof(arrX) / sizeof(arrX[0]); // Размер массива X

    // Вычисляем размер Y: каждый второй элемент из X по условию задачи
    int sizeY = (sizeX + 1) / 2; // (n+1)/2 чтобы учитывать вероятность,что массив sizeX нечетного размера
    int arrY[sizeY]; // Создаем Массив Y
    int indexY = 0;// счётчик для y

    // Записываем каждый второй элемент из X в Y
    for (int i = 1; i < sizeX; i += 2) { // начинаем с 1 для получения x2, x4, x6... элемента
        arrY[indexY++] = arrX[i];//заменяет элемент в arrY на текущий элемент arrX[i], а затем увеличивает индекс indexY на 1 для следующей итерации.
    }

    // Подсчет простых чисел в X
    int CountX = 0;//переменная для хранения простых чисел в массиве x,счётчик
    for (int i = 0; i < sizeX; i++) {
        if (isPrime(arrX[i])) {//вызов функции которая проверяет число является простым
            CountX++;//если условие выполняется ,то +1 в счётчик
        }
    }

    // Подсчет простых чисел в Y,аналогично продсчёту простых чиселв в X
    int CountY = 0;
    for (int i = 0; i < indexY; i++) {
        if (isPrime(arrY[i])) {
            CountY++;
        }
    }

    // Вычисляем среднее арифметическое для массива X
    double sumX = 0;//переменная для хранения суммы всех элементов массива arrX
    for (int i = 0; i < sizeX; i++) {
        sumX += arrX[i];//подсчёт всех элементов в массиве 
    }
    double averageX = sumX / sizeX; //находим среднее арифметическое массива X сумма всех элементов мы делим на количество элементов

    // Вычисляем среднее арифметическое для массива Y,аналогично X
    double sumY = 0;
    for (int i = 0; i < indexY; i++) {
        sumY += arrY[i];
    }
    double averageY;
    if (indexY > 0) {
        averageY = sumY / indexY; // Если indexY больше 0, то среднее ариметическое
    } else {
        averageY = 0; // Если нет заполненных элементов, то среднее равно 0
    }
    // Вывод результатов пользователю
    printf("Количество простых чисел в массиве X: %d\n", CountX);
    printf("Количество простых чисел в массиве Y: %d\n", CountY);
    printf("Среднее арифметическое массива X: %.2f\n", averageX);//ответ с плавающей точкой
    printf("Среднее арифметическое массива Y: %.2f\n", averageY);//ответ с плавающей точкой

    return 0;//завершение программы
}
//Количество простых чисел в массиве X: 3 Это(2,3 и 7)
//Количество простых чисел в массиве Y: 1 Из(2,4,12)=>Простое число 2
//Среднее арифметическое массива X: 16.71 (117/7)
//Среднее арифметическое массива Y: 6.00  (2+4+12=18/3)